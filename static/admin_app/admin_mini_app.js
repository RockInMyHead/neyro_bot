async function processNextBatch() {
    const btn = event.target;
    const originalText = btn.textContent;
    
    try {
        btn.disabled = true;
        btn.textContent = '‚è≥ –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º...';
        
        const response = await fetch('/api/admin/queue/process', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            }
        });
        
        const data = await response.json();
        
        if (data.success) {
            showNotification('–ë–∞—Ç—á —É—Å–ø–µ—à–Ω–æ –æ–±—Ä–∞–±–æ—Ç–∞–Ω!', 'success');
            refreshQueueStats();
            refreshBatchStatus();
        } else {
            showNotification(data.message || '–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –±–∞—Ç—á–∞', 'warning');
        }
    } catch (error) {
        console.error('–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –±–∞—Ç—á–∞:', error);
        showNotification('–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –±–∞—Ç—á–∞', 'error');
    } finally {
        btn.disabled = false;
        btn.textContent = originalText;
    }
}

async function refreshBatchStatus() {
    try {
        const response = await fetch('/api/admin/queue/batch-status', {
            method: 'GET',
            headers: {
                'Content-Type': 'application/json'
            }
        });
        const data = await response.json();
        
        if (data.success && data.batch_status) {
            displayBatchStatus(data.batch_status);
        } else {
            document.getElementById('batchStatus').style.display = 'none';
        }
    } catch (error) {
        console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Å—Ç–∞—Ç—É—Å–∞ –±–∞—Ç—á–∞:', error);
    }
}

